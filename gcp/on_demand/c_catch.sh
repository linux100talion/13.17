#!/bin/bash
# «Ловля» on-demand GPU (Tesla T4) инстанса в зоне europe-west4-c: гоняет
# ./c_start.sh по кругу, пока зона не отдаст T4.
#
# ТОНКАЯ обёртка над c_start.sh — вся логика старта (проверки зоны/статуса,
# собственно `gcloud instances start`) живёт там. Этот скрипт лишь повторяет
# запуск каждые 5 минут, пока ёмкость не появится.
#
# Повтор только при дефиците ёмкости (RESOURCE_POOL_EXHAUSTED). Любая другая
# причина незапуска (инстанса нет / он в другой зоне) ретраем не лечится —
# тогда скрипт сразу останавливается и показывает вывод c_start.sh.
#
# Тогглы (env):
#   INTERVAL=300   пауза между попытками, сек (по умолчанию 5 минут)
#   MAX=0          лимит попыток (0 = без лимита, ловить до победного)
#
# Три зоны — три скрипта (a/b/c_catch.sh), отличаются только вызовом a/b/c_start.sh.

set -u

INTERVAL="${INTERVAL:-300}"
MAX="${MAX:-0}"
SELF_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
START="$SELF_DIR/c_start.sh"     # ← единственное, чем отличаются a/b/c_catch.sh

i=0
while :; do
    i=$((i + 1))
    TS=$(date '+%Y-%m-%d %H:%M:%S')
    echo "════ попытка $i ($TS) ════"

    OUT=$("$START" 2>&1); RC=$?
    echo "$OUT"

    if [ $RC -eq 0 ]; then
        echo "🎯 Поймали! Инстанс запущен (или уже работал). SSH: ./c_ssh.sh"
        exit 0
    fi

    # Ретраим только дефицит ёмкости; прочие ошибки повтором не исправить.
    if ! echo "$OUT" | grep -qiE "EXHAUSTED|ZONE_RESOURCE"; then
        echo "❌ Незапуск не из-за дефицита T4 — ретрай не поможет, останавливаюсь."
        exit 1
    fi

    if [ "$MAX" -gt 0 ] && [ "$i" -ge "$MAX" ]; then
        echo "⏹️  Исчерпал лимит попыток ($MAX). T4 в зоне так и не появился."
        exit 1
    fi

    echo "⏳ Нет T4 — жду ${INTERVAL}с до следующей попытки..."
    sleep "$INTERVAL"
done
